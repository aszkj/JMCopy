+ (instancetype)shardManager{
    
    static BFUserInfoEditDataSourceManager *_instance;
    static dispatch_once_t onceToken;
    
    dispatch_once(&onceToken, ^{
        _instance = [[BFUserInfoEditDataSourceManager alloc] init];
        [_instance loadDataSourceFromPlist];
    });
    
    return _instance;
}

- (void)loadDataSourceFromPlist{
    
    //先从本地偏好设置加载数据源
    //    self.totalDataSourceDict = USER_INFODATA_DICT;
    
    //如果本地没有则加载代码中的数据 并且存到本地一份
    if(self.totalDataSourceDict == nil){
        [self setDataSourceForFirstTime];
        [self saveTotalDataSourceDict];
    }
    
    //将总数据源字典中的数组全部替换成可变的
    [self enumAllArrToArrM];
}

//将当前内存中的用户编辑对应的总的数据源字典存到用户偏好设置
- (void)saveTotalDataSourceDict{
    [[NSUserDefaults standardUserDefaults]setObject:self.totalDataSourceDict forKey:USER_INFODATA_KEY];
}

//将字典中对应的数组全部替换成可变数组
- (void)enumAllArrToArrM{
    NSMutableDictionary *dictM = [NSMutableDictionary dictionary];
    [self.totalDataSourceDict enumerateKeysAndObjectsUsingBlock:^(id  _Nonnull key, id  _Nonnull obj, BOOL * _Nonnull stop) {
        NSMutableArray *arrM = [obj mutableCopy];
        [dictM setValue:arrM forKey:key];
    }];
    self.totalDataSourceDict = dictM;
}


